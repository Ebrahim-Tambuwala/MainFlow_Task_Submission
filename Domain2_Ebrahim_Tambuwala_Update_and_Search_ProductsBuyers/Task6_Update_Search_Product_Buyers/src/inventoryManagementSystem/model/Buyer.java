package inventoryManagementSystem.model;

public class Buyer {

    private int id;
    private String name;
    private String email;
    private String phone;
    private String address;

    // Default Constructor (Professional Practice)
    public Buyer() {
    }

    // Constructor for Adding New Buyer
    public Buyer(String name, String email, String phone, String address) {
        this.name = name;
        this.email = email;
        this.phone = phone;
        this.address = address;
    }

    // Full Constructor (Used for Update & Advanced Features)
    public Buyer(int id, String name, String email, String phone, String address) {
        this.id = id;
        this.name = name;
        this.email = email;
        this.phone = phone;
        this.address = address;
    }

    // Getters
    public int getId() { return id; }
    public String getName() { return name; }
    public String getEmail() { return email; }
    public String getPhone() { return phone; }
    public String getAddress() { return address; }

    // Setters (Required for Update Feature)
    public void setId(int id) { this.id = id; }
    public void setName(String name) { this.name = name; }
    public void setEmail(String email) { this.email = email; }
    public void setPhone(String phone) { this.phone = phone; }
    public void setAddress(String address) { this.address = address; }
}